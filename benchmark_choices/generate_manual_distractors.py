def _match(text: str, source: str) -> re.Match[str] | None:
    return re.search(rf"(?<!\w){re.escape(source)}(?!\w)", text, flags=re.IGNORECASE)


def _replace_once(text: str, source: str, replacement: str) -> str:
    return re.sub(
        rf"(?<!\w){re.escape(source)}(?!\w)",
        replacement,
        text,
        count=1,
        flags=re.IGNORECASE,
    )


def choose_attributes(prompt: str) -> list[dict[str, str]]:
    chosen: list[dict[str, str]] = []
    occupied: list[tuple[int, int]] = []
    used_categories: set[str] = set()
    for category, source, replacement in REPLACEMENTS:
        match = _match(prompt, source)
        if match is None:
            continue
        if any(match.start() < end and match.end() > start for start, end in occupied):
            continue
        if category in used_categories and len(chosen) == 1:
            continue
        chosen.append(
            {
                "category": category,
                "source": match.group(0),
                "replacement": replacement,
            }
        )
        occupied.append(match.span())
        used_categories.add(category)
        if len(chosen) == 2:
            return chosen
    raise ValueError(f"Could not select two distinct attributes: {prompt!r}")


def make_distractors(prompt: str, attributes: list[dict[str, str]]) -> list[str]:
    first, second = attributes
    changed_first = _replace_once(prompt, first["source"], first["replacement"])
    changed_second = _replace_once(prompt, second["source"], second["replacement"])
    changed_both = _replace_once(changed_first, second["source"], second["replacement"])
    choices = [prompt, changed_first, changed_second, changed_both]
    if len(set(choices)) != 4:
        raise ValueError(f"Substitutions did not produce four unique choices: {prompt!r}")
    return choices[1:]


def main() -> None:
    for root in CHOICE_ROOTS:
        for path in sorted(root.glob("*.json")):
            payload = json.loads(path.read_text(encoding="utf-8"))
            for row in payload["rows"]:
                prompt = str(row["correct_prompt"])
                attributes = choose_attributes(prompt)
                row["attributes"] = attributes
                row["distractors"] = make_distractors(prompt, attributes)
            path.write_text(
                json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
                encoding="utf-8",
            )
            print(f"Curated {len(payload['rows'])} rows in {path.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
